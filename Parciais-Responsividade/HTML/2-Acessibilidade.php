                <h2>Resumo</h2>
                Na atual era da transformação digital, ter um site acessível demonstra que a
                empresa preza pela inclusão e tem uma estratégia inteligente de negócio. Na prática,
                a acessibilidade para sites garante que pessoas com deficiência possam
                navegar e interagir com suas páginas da web de forma independente e eficiente.

                Além disso, empresas que investem em sites acessíveis ampliam seu
                alcance, melhoram a experiência do usuário e fortalecem sua reputação no
                mercado.

                <h2>Como construir um site acessível?</h2>

                Criar um site acessível envolve uma série de boas práticas que garantem que
                todos tenham uma experiência completa. A seguir, apontamos e explicamos alguns
                passos essenciais para ter sucesso na construção de um site mais acessível:
                Use um design responsivo
                Um design responsivo é fundamental para que o site se ajuste a diferentes
                tamanhos de tela e dispositivos, como smartphones, tablets e computadores. Além de
                melhorar a experiência do usuário, ajuda as pessoas com deficiências motoras ou
                visuais a navegarem com mais facilidade.

                <h2>Ofereça descrições alternativas para imagens</h2>

                A descrição alternativa (ou alt text) é um texto curto que descreve o conteúdo
                e a função de uma imagem em uma página da web. Ela é invisível para os usuários
                que conseguem ver as imagens, mas é lida por leitores de tela e para aqueles que
                não podem visualizá-las.
                Sendo assim, as descrições são essenciais para garantir a acessibilidade de
                sites, permitindo que pessoas com deficiência visual utilizem leitores de tela para
                compreender o conteúdo visual


                <h2>Como criar uma boa descrição alternativa?</h2>
                • Seja descritivo, mas conciso: descreva o que é essencial na imagem de
                forma objetiva, sem entrar em muitos detalhes que possam ser irrelevantes
                para o contexto;<br><br>
                • Informe o propósito da imagem: se a imagem tem uma função específica,
                como um botão ou link, explique o que acontece ao interagir com ela;<br><br>
                • Evite expressões como “imagem de” ou “foto de”: o leitor de tela já sabe
                que se trata de uma imagem, então, vá direto ao ponto; <br><br>
                • Considere o contexto da página: a descrição deve refletir como a imagem
                contribui para o conteúdo ao redor; <br><br>
                Veja um bom exemplo de descrição alternativa para uma imagem de um
                cachorro brincando no parque: “Cachorro preto correndo na grama verde com uma
                bola na boca”.

                <h2>Garanta o contraste adequado</h2>
                Para pessoas com deficiência visual ou outras necessidades de leitura, o
                contraste entre o texto e o fundo da página é fundamental. Sendo assim, certifique-se
                de que as cores de seu site tenham contraste suficiente para garantir que o conteúdo
                seja legível por todos os usuários.

                <h2>Facilite a navegação por teclado</h2>
                Muitas pessoas com deficiência motoras não utilizam o mouse para navegar.
                Portanto, é essencial que todas as funcionalidades do site sejam acessíveis por meio
                do teclado, incluindo links, menus e formulários.

                <h2>Insira legendas em vídeos</h2>
                Vídeos são uma ferramenta poderosa de comunicação, e é necessário garantir
                que todo o público entenda o conteúdo. Para tanto, adicione legendas em vídeos para
                facilitar a compreensão de pessoas com deficiência auditiva.

